#ifndef VKFS_SHAPECONSTRUCTOR_H
#define VKFS_SHAPECONSTRUCTOR_H

#include <iostream>
#include <vector>
#include <cmath>
#include <vulkan/vulkan.h>

namespace VKFS::EXT {

    struct ShapeVertex {
        float position[3];
        float normal[3];
        float texCoords[2];

        static VkVertexInputBindingDescription getBindingDescription() {
            VkVertexInputBindingDescription bindingDescription{};
            bindingDescription.binding = 0;
            bindingDescription.stride = sizeof(ShapeVertex);
            bindingDescription.inputRate = VK_VERTEX_INPUT_RATE_VERTEX;

            return bindingDescription;
        }

        static std::vector<VkVertexInputAttributeDescription> getAttributeDescriptions() {
            std::vector<VkVertexInputAttributeDescription> attributeDescriptions{};
            attributeDescriptions.resize(3);

            attributeDescriptions[0].binding = 0;
            attributeDescriptions[0].location = 0;
            attributeDescriptions[0].format = VK_FORMAT_R32G32B32_SFLOAT;
            attributeDescriptions[0].offset = offsetof(ShapeVertex, position);

            attributeDescriptions[1].binding = 0;
            attributeDescriptions[1].location = 1;
            attributeDescriptions[1].format = VK_FORMAT_R32G32B32_SFLOAT;
            attributeDescriptions[1].offset = offsetof(ShapeVertex, normal);

            attributeDescriptions[2].binding = 0;
            attributeDescriptions[2].location = 2;
            attributeDescriptions[2].format = VK_FORMAT_R32G32_SFLOAT;
            attributeDescriptions[2].offset = offsetof(ShapeVertex, texCoords);

            return attributeDescriptions;
        }

    };

    struct ShapeInfo {
        std::vector<ShapeVertex> vertices;
        std::vector<uint32_t> indices;
    };

    class ShapeConstructor {
        public:
            static ShapeInfo createSphere(int segments, int rings);
    };

}


#endif //VKFS_SHAPECONSTRUCTOR_H
